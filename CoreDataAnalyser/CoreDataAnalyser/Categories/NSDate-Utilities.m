// Add ordinal to number ie 1st, 2nd, 3rd, etc.
- (NSString *) shortDateWithOrdinalString
{
    NSString *suffix = nil;
    
    int ones = self.day % 10;
    int tens = floor(self.day / 10);
    tens = tens % 10;
    
    if (tens == 1)
    {
        suffix = @"th";
    }
    else
    {
        switch (ones)
        {
            case 1:
                suffix = @"st";
                break;
                
            case 2:
                suffix = @"nd";
                break;
                
            case 3:
                suffix = @"rd";
                break;
                
            default:
                suffix = @"th";
        }
    }
    
    return [NSString stringWithFormat: @"%d%@ %@", self.day, suffix, self.shortMonthString];
}

- (NSString *) shortDateWithOrdinalStringAndYear
{
    return [NSString stringWithFormat: @"%@ %@", self.shortDateWithOrdinalString, self.shortYearString];
}

